import math #Importamos la libreria math para saber el valor de PI

radio = float(input("Introduce el radio de la circunferencia"))
longitud = 2*math.pi*radio #Formulas
area = math.pi*radio**2 #Formulas
print("La longitud de la circunferencia es ", longitud)
print("El area de la circunferencia es ", area) 
