# Calcula la suma de los primeros 10 numeros enteros positivos

suma = 0 # Inicializamos la variable suma en 0

for i in range(1, 11): # Generamos los numeros del 1 al 10
    suma+= i # Definimos la suma que es la suma de los numeros del 1 al 10

print("La suma de los primeros 10 numeros enteros positivos es:", suma) # Imprimimos la solucion 
